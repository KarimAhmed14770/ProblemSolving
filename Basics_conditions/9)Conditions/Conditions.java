import java.lang.*;
import java.util.*;




public class Conditions{
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        byte a,b;
        a=sc.nextByte();
        b=sc.nextByte();
        if(a>=b)
        {
            System.out.print("Yes");
        }
        else
        {
            System.out.print("No");
        }

    }
}